<h2 id="智能体的核心定义" tabindex="-1"><a class="header-anchor" href="#智能体的核心定义"><span>智能体的核心定义</span></a></h2>
<p>智能体（Agent）的核心定义是一个能够感知环境、自主决策、主动行动和学习进化的 AI 程序。这个定义包含了智能体实现其功能所需的四大关键能力：感知、决策、行动和学习。
<img src="/img/user/附件/Pasted image 20250504185726.png" alt="Pasted image 20250504185726.png"></p>
<h3 id="感知-智能体的-眼睛" tabindex="-1"><a class="header-anchor" href="#感知-智能体的-眼睛"><span>感知：智能体的“眼睛”</span></a></h3>
<p>感知是智能体与外界环境交互的基础。通过感知，智能体能够获取环境信息，为后续决策提供依据。常见的感知方式包括：</p>
<ul>
<li><strong>视觉感知</strong>：通过摄像头捕捉图像。</li>
<li><strong>听觉感知</strong>：通过麦克风接收声音。</li>
</ul>
<h3 id="决策-智能体的-大脑" tabindex="-1"><a class="header-anchor" href="#决策-智能体的-大脑"><span>决策：智能体的“大脑”</span></a></h3>
<p>决策是智能体进行自主行动的核心环节，其背后依赖于复杂的算法和模型。例如：</p>
<ul>
<li><strong>深度学习</strong>：通过神经网络从大量数据中提取特征并进行推断。</li>
<li><strong>强化学习</strong>：通过试错和反馈机制优化决策策略。
<img src="/img/user/附件/Pasted image 20250504185737.png" alt="Pasted image 20250504185737.png"></li>
</ul>
<h1 id="与其讨论-agent-不如讨论-agentic" tabindex="-1"><a class="header-anchor" href="#与其讨论-agent-不如讨论-agentic"><span>与其讨论 Agent，不如讨论 Agentic</span></a></h1>
<p>在大模型（Large Language Model，LLM）领域，技术发展日新月异，而关于“Agent”（智能体）的定义，目前并没有一个统一的说法。尤其是在主流的基于 LLM 的 Agent 设计中，各种实现方式和理解层出不穷。因此，吴恩达（Andrew Ng）和 Horrison 等人提出了一个非常值得深思的观点：与其争论某个系统是否属于智能体，不如承认这个世界并非非黑即白。一个系统可以是渐进的，可以具备不同程度的智能体属性（Agentic）。这就像自动驾驶汽车有 L1-L4 不同级别的自主性一样。</p>
<p>本文将围绕系统的智能体属性（Agentic）展开讨论，并分析其不同层级的实现方式。
<img src="/img/user/附件/Pasted image 20250504185847.png" alt="Pasted image 20250504185847.png"></p>
<hr>
<h2 id="系统的-agentic-程度由什么决定" tabindex="-1"><a class="header-anchor" href="#系统的-agentic-程度由什么决定"><span>系统的 Agentic 程度由什么决定？</span></a></h2>
<p>一个系统的 Agentic 程度，主要取决于 LLM 对系统行为决策的权重。换句话说，LLM 在系统中承担了多大程度的自主性和决策能力，直接影响了该系统在智能体属性上的表现。</p>
<p>接下来，我们将通过几个典型的层级，逐步剖析 Agentic 的不同实现方式。</p>
<hr>
<h3 id="_1-初步的-agentic-路由器-router" tabindex="-1"><a class="header-anchor" href="#_1-初步的-agentic-路由器-router"><span>1. 初步的 Agentic：路由器（Router）</span></a></h3>
<p>当一个系统使用 LLM 将输入路由到特定的下游工作流时，它具备了初步的 Agentic 属性。这种情况下，LLM 的作用类似于一个“路由器”，它根据输入内容的特征，选择合适的路径或模块来处理任务。</p>
<p>这一层级的特点是：</p>
<ul>
<li>LLM 仅起到简单的分发作用；</li>
<li>系统的行为是线性的，没有复杂的决策逻辑；</li>
<li>虽然具备一定智能性，但整体自主性较低。</li>
</ul>
<hr>
<h3 id="_2-多级路由决策-介于路由器与状态机之间" tabindex="-1"><a class="header-anchor" href="#_2-多级路由决策-介于路由器与状态机之间"><span>2. 多级路由决策：介于路由器与状态机之间</span></a></h3>
<p>如果一个系统使用多个 LLM 进行多级路由决策，那么它在 Agentic 属性上更进一步。这种设计允许系统在不同阶段调用不同的模型或模块，从而实现更复杂的任务分配和处理。</p>
<p>这一层级可以被视为介于“路由器”和“状态机”之间。相比单一 LLM 路由器，多级路由决策增加了以下能力：</p>
<ul>
<li>在多个步骤中动态调整任务流程；</li>
<li>根据上下文信息灵活选择下一步操作；</li>
<li>系统开始表现出一定程度的非线性行为。</li>
</ul>
<hr>
<h3 id="_3-状态机-state-machine-允许循环运行" tabindex="-1"><a class="header-anchor" href="#_3-状态机-state-machine-允许循环运行"><span>3. 状态机（State Machine）：允许循环运行</span></a></h3>
<p>当一个系统能够在某些步骤中决定是继续执行还是终止任务时，它就进入了状态机（State Machine）的范畴。状态机的核心特性在于：</p>
<ul>
<li>系统可以根据当前状态和上下文信息，决定进入下一个状态；</li>
<li>允许循环运行，直到完成任务或满足某个条件。</li>
</ul>
<p>例如，一个状态机型系统可能会反复检查任务进展，并动态调整策略，直到目标达成。这种能力显著增强了系统的自主性，使其具备更高程度的 Agentic 属性。</p>
<hr>
<h3 id="_4-自主-agent-autonomous-agent-顶层智能体" tabindex="-1"><a class="header-anchor" href="#_4-自主-agent-autonomous-agent-顶层智能体"><span>4. 自主 Agent（Autonomous Agent）：顶层智能体</span></a></h3>
<p>当系统能够自主构建工具、记忆工具并在后续步骤中使用时，它就达到了 Agentic 的顶层——自主 Agent（Autonomous Agent）。这种系统不仅能够完成复杂任务，还能在执行过程中：</p>
<ul>
<li>动态生成新的工具或方法；</li>
<li>将生成的工具存储为记忆，以便未来使用；</li>
<li>在任务执行中不断学习和优化自身行为。</li>
</ul>
<p>自主 Agent 的出现标志着系统从被动执行者转变为主动决策者，其智能体属性已接近真正意义上的自主智能体。</p>
